/**
 * Interactive FWE/Power trade-off widget.
 * Used in Tutorial 04.
 *
 * Exports:
 *   fwePowerWidget() — slider adjusts number of groups; two bar charts show
 *                       family-wise error rate and power for Tukey vs unadjusted tests
 */

// --- Shared utilities -------------------------------------------------------

function svgEl(tag, attrs) {
  const ns = "http://www.w3.org/2000/svg";
  const el = document.createElementNS(ns, tag);
  for (const [k, v] of Object.entries(attrs)) el.setAttribute(k, v);
  return el;
}

function makeBox() {
  const box = document.createElement("div");
  box.style.cssText =
    "background:#fff;border:2px solid #333;border-radius:6px;" +
    "padding:16px 20px;width:90%;" +
    "font-family:system-ui,sans-serif;font-size:16px;color:#222;";
  return box;
}


