// =============================================================================
// FWE / Power Trade-off Explorer
// =============================================================================

/**
 * Two side-by-side bar charts showing family-wise error rate (left) and
 * statistical power (right) as the number of groups changes via a slider.
 *
 * @param {Object} [opts]
 * @returns {HTMLElement}
 */
export function fwePowerWidget(opts = {}) {
  const {
    colorTukey = "#e64626",
    colorUnadj = "#404040",
  } = opts;

  const panelW = 280, panelH = 200;
  const margin = { top: 30, right: 10, bottom: 40, left: 50 };
  const barAreaW = panelW - margin.left - margin.right;
  const barAreaH = panelH - margin.top - margin.bottom;

  let nGroups = 5;

  const box = makeBox();

  // --- Slider ---
  const sliderLabel = document.createElement("div");
  sliderLabel.style.cssText =
    "font-size:15px;font-weight:600;color:#222;margin-bottom:4px;";
  box.appendChild(sliderLabel);

  const ctrl = document.createElement("div");
  ctrl.style.cssText = "display:flex;align-items:center;gap:12px;margin-bottom:12px;";
  ctrl.innerHTML =
    '<span style="font-size:14px;color:#666">2</span>' +
    `<input type="range" min="2" max="10" value="${nGroups}" step="1" ` +
    `aria-label="Number of groups" ` +
    `style="flex:1;accent-color:${colorTukey};height:6px">` +
    '<span style="font-size:14px;color:#666">10</span>';
  box.appendChild(ctrl);

  const slider = ctrl.querySelector("input");

  // --- SVG container (two panels side by side) ---
  const svgContainer = document.createElement("div");
  svgContainer.style.cssText = "display:flex;gap:16px;flex-wrap:wrap;justify-content:center;";
  box.appendChild(svgContainer);

  // Create two SVG panels
  function createPanel(title, yMax, showAlphaLine) {
    const svg = svgEl("svg", { viewBox: `0 0 ${panelW} ${panelH}` });
    svg.style.cssText = "display:inline-block;width:100%;max-width:280px;";

    // Title
    const titleEl = svgEl("text", {
      x: panelW / 2, y: 16,
      "text-anchor": "middle", fill: "#444", "font-size": "13px", "font-weight": "600",
    });
    titleEl.textContent = title;
    svg.appendChild(titleEl);

    // Y-axis
    svg.appendChild(svgEl("line", {
      x1: margin.left, y1: margin.top,
      x2: margin.left, y2: margin.top + barAreaH,
      stroke: "#999", "stroke-width": "1",
    }));

    // Y-axis ticks and labels
    const nTicks = 5;
    for (let i = 0; i <= nTicks; i++) {
      const val = (yMax / nTicks) * i;
      const y = margin.top + barAreaH - (val / yMax) * barAreaH;

      svg.appendChild(svgEl("line", {
        x1: margin.left - 4, y1: y, x2: margin.left, y2: y,
        stroke: "#999", "stroke-width": "1",
      }));

      const label = svgEl("text", {
        x: margin.left - 8, y: y + 4,
        "text-anchor": "end", fill: "#666", "font-size": "11px",
      });
      label.textContent = (val * 100).toFixed(0) + "%";
      svg.appendChild(label);
    }

    // X-axis
    svg.appendChild(svgEl("line", {
      x1: margin.left, y1: margin.top + barAreaH,
      x2: margin.left + barAreaW, y2: margin.top + barAreaH,
      stroke: "#999", "stroke-width": "1",
    }));

    // Alpha reference line at 0.05
    if (showAlphaLine) {
      const alphaY = margin.top + barAreaH - (0.05 / yMax) * barAreaH;
      svg.appendChild(svgEl("line", {
        x1: margin.left, y1: alphaY,
        x2: margin.left + barAreaW, y2: alphaY,
        stroke: "#999", "stroke-width": "1.5", "stroke-dasharray": "6,3",
      }));
      const alphaLabel = svgEl("text", {
        x: margin.left + barAreaW + 2, y: alphaY + 4,
        "text-anchor": "start", fill: "#999", "font-size": "10px",
      });
      alphaLabel.textContent = "α = 0.05";
      svg.appendChild(alphaLabel);
    }

    // Dynamic bar group
    const barGroup = svgEl("g", {});
    svg.appendChild(barGroup);

    return { svg, barGroup, yMax };
  }

  const fwePanel = createPanel("Family-wise error rate", 0.7, true);
  const powerPanel = createPanel("Statistical power", 1.0, false);

  const fweWrap = document.createElement("div");
  fweWrap.style.cssText = "flex:1;min-width:240px;max-width:300px;";
  fweWrap.appendChild(fwePanel.svg);
  svgContainer.appendChild(fweWrap);

  const powerWrap = document.createElement("div");
  powerWrap.style.cssText = "flex:1;min-width:240px;max-width:300px;";
  powerWrap.appendChild(powerPanel.svg);
  svgContainer.appendChild(powerWrap);

  // --- Legend ---
  const legend = document.createElement("div");
  legend.style.cssText =
    "display:flex;gap:20px;justify-content:center;margin-top:10px;font-size:14px;";
  legend.innerHTML =
    `<span><span style="display:inline-block;width:14px;height:14px;background:${colorTukey};border-radius:2px;vertical-align:middle;margin-right:4px;"></span> Tukey-adjusted</span>` +
    `<span><span style="display:inline-block;width:14px;height:14px;background:${colorUnadj};border-radius:2px;vertical-align:middle;margin-right:4px;"></span> Unadjusted t-tests</span>`;
  box.appendChild(legend);

  // --- Stats line ---
  const stats = document.createElement("div");
  stats.style.cssText =
    "margin-top:10px;padding-top:10px;border-top:2px solid #ddd;" +
    "font-size:15px;color:#222;text-align:center;";
  box.appendChild(stats);

  // --- Draw bars ---
  function drawBars(panel, valLeft, valRight) {
    const { barGroup, yMax } = panel;
    while (barGroup.firstChild) barGroup.removeChild(barGroup.firstChild);

    const barW = barAreaW * 0.25;
    const gap = barAreaW * 0.1;
    const totalW = 2 * barW + gap;
    const startX = margin.left + (barAreaW - totalW) / 2;

    // Left bar (Tukey)
    const h1 = (valLeft / yMax) * barAreaH;
    barGroup.appendChild(svgEl("rect", {
      x: startX, y: margin.top + barAreaH - h1,
      width: barW, height: Math.max(1, h1),
      fill: colorTukey, rx: "2",
    }));

    // Left bar label
    const label1 = svgEl("text", {
      x: startX + barW / 2, y: margin.top + barAreaH - h1 - 6,
      "text-anchor": "middle", fill: colorTukey, "font-size": "12px", "font-weight": "600",
    });
    label1.textContent = (valLeft * 100).toFixed(1) + "%";
    barGroup.appendChild(label1);

    // Left bar x-label
    const xLabel1 = svgEl("text", {
      x: startX + barW / 2, y: margin.top + barAreaH + 16,
      "text-anchor": "middle", fill: "#666", "font-size": "11px",
    });
    xLabel1.textContent = "Tukey";
    barGroup.appendChild(xLabel1);

    // Right bar (Unadjusted)
    const x2 = startX + barW + gap;
    const h2 = (valRight / yMax) * barAreaH;
    barGroup.appendChild(svgEl("rect", {
      x: x2, y: margin.top + barAreaH - h2,
      width: barW, height: Math.max(1, h2),
      fill: colorUnadj, rx: "2",
    }));

    // Right bar label
    const label2 = svgEl("text", {
      x: x2 + barW / 2, y: margin.top + barAreaH - h2 - 6,
      "text-anchor": "middle", fill: colorUnadj, "font-size": "12px", "font-weight": "600",
    });
    label2.textContent = (valRight * 100).toFixed(1) + "%";
    barGroup.appendChild(label2);

    // Right bar x-label
    const xLabel2 = svgEl("text", {
      x: x2 + barW / 2, y: margin.top + barAreaH + 16,
      "text-anchor": "middle", fill: "#666", "font-size": "11px",
    });
    xLabel2.textContent = "Unadj.";
    barGroup.appendChild(xLabel2);
  }

  // --- Update ---
  function update() {
    const d = DATA[nGroups];
    const nComparisons = nGroups * (nGroups - 1) / 2;

    sliderLabel.textContent = `Number of groups: ${nGroups}`;

    drawBars(fwePanel, d.fweTukey, d.fweUnadj);
    drawBars(powerPanel, d.powerTukey, d.powerUnadj);

    stats.innerHTML =
      `<b>${nComparisons}</b> pairwise comparison${nComparisons === 1 ? "" : "s"} &middot; ` +
      `<span style="color:#999">C(${nGroups}, 2) = ${nGroups} &times; ${nGroups - 1} / 2</span>`;
  }

  slider.oninput = () => {
    nGroups = +slider.value;
    update();
  };

  update();
  return box;
}
